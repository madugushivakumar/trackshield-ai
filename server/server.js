require("dotenv").config();

// =====================================
// IMPORT APP
// =====================================
const app =
  require("./src/app");

// =====================================
// DATABASE
// =====================================
const connectDB =
  require("./src/config/db");

// =====================================
// CONNECT DATABASE
// =====================================
connectDB();

